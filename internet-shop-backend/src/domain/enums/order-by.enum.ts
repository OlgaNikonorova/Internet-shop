/**
 * Enum representing the sorting order for data.
 */
export enum OrderBy {
  /**
   * Represents ascending order.
   * Typically used to sort data from smallest to largest or alphabetically (A-Z).
   */
  Ascending = 'ascending',

  /**
   * Represents descending order.
   * Typically used to sort data from largest to smallest or reverse alphabetically (Z-A).
   */
  Descending = 'descending',
}
