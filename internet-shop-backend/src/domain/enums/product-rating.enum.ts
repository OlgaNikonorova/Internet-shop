/**
 * Enum representing the possible product ratings.
 * Each rating corresponds to a number of stars, ranging from 1 to 5.
 *
 * @enum {number}
 * @property {number} ONE_STAR - Represents a 1-star rating.
 * @property {number} TWO_STARS - Represents a 2-star rating.
 * @property {number} THREE_STARS - Represents a 3-star rating.
 * @property {number} FOUR_STARS - Represents a 4-star rating.
 * @property {number} FIVE_STARS - Represents a 5-star rating.
 */
export enum ProductRating {
  ONE_STAR = 1,
  TWO_STARS,
  THREE_STARS,
  FOUR_STARS,
  FIVE_STARS,
}
